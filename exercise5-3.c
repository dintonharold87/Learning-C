/* Given three integers, 15, 150, and 1500, write a program that prints the integers on
the screen in the hex format. */
#include<stdio.h>
int main(){
int a,b,c;
a=15;
b=150;
c=1500;
printf("The hex format of %d is %x\n",a,a);
printf("The hex format of %d is %x\n",b,b);
printf("The hex format of %d is %x\n",c,c);
return 0;
}